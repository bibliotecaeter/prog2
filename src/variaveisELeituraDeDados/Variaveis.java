package variaveisELeituraDeDados;

public class Variaveis {
	/*
	 * Lembre-se: sempre que for necessário executar algo, você deverá colocar o código dentro de um método main
	 * O método main é o método principal
	 */
	
	public static void main(String[] args) {
		
		// Variavies inteiras
		byte idade1 = 10;		
		short idade2 = 11;		
		int idade3 = 12;		
		long idade4 = 13;		
		System.out.println("Valor variável idade1 = " + idade1);
		System.out.println("Valor variável idade2 = " + idade2);
		System.out.println("Valor variável idade3 = " + idade3);
		System.out.println("Valor variável idade4 = " + idade4);
		
		// Variaveis de ponto flutuante
		double valorPassagem = 2.90;		
		float valorTomate = 3.95F;		
		System.out.println("Valor da passagem = " + valorPassagem);
		System.out.println("Valor do tomate = " + valorTomate);
		
		// Variaveis booleanas
		boolean verdadeiro = true;		
		boolean falso = false;
		System.out.println("O valor de verdadeiro é " + verdadeiro);
		System.out.println("O valor de false é " + falso);
		
	}

}
